package com.example.tugasakhir.Model;

public class RespondenModel {
    String norspnd, nama_rspnd, jenis_kelamin, tempat_lahir, tgl_lahir, alamat;

    public String getNorspnd() {
        return norspnd;
    }

    public void setNorspnd(String norspnd) {
        this.norspnd = norspnd;
    }

    public String getNama_rspnd() {
        return nama_rspnd;
    }

    public void setNama_rspnd(String nama_rspnd) {
        this.nama_rspnd = nama_rspnd;
    }

    public String getJenis_kelamin() {
        return jenis_kelamin;
    }

    public void setJenis_kelamin(String jenis_kelamin) {
        this.jenis_kelamin = jenis_kelamin;
    }

    public String getTempat_lahir() {
        return tempat_lahir;
    }

    public void setTempat_lahir(String tempat_lahir) {
        this.tempat_lahir = tempat_lahir;
    }

    public String getTgl_lahir() {
        return tgl_lahir;
    }

    public void setTgl_lahir(String tgl_lahir) {
        this.tgl_lahir = tgl_lahir;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
